package com.example.ninjabocil.models;

public class skill {

    private String nama, grade, damage, chakra, procRate, fotoskill, preview, effect, launch, restriction, round;

    public String getNama()
    {
        return nama;
    }

    public String getGrade()
    {
        return grade;
    }

    public String getDamage()
    {
        return damage;
    }

    public String getPreview() {
        return preview;
    }

    public String getEffect() {
        return effect;
    }

    public String getLaunch() {
        return launch;
    }

    public String getRestriction() {
        return restriction;
    }

    public String getRound() {
        return round;
    }

    public String getChakra()
    {
        return chakra;
    }

    public String getProcRate()
    {
        return procRate;
    }

    public String getImage()
    {
        return fotoskill;
    }
}
